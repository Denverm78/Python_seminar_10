class Rectangle:
    def __init__(self, a, b=None):
        if b == None:
            b = a
        self.a = a
        self.b = b

    def perimeter(self):
        return (self.a + self.b) * 2

    def sq(self):
        return self.a * self.b


rec = Rectangle(10, 5)
print('–°—Ç–æ—Ä–æ–Ω–∞ a = ', rec.a)
print('–°—Ç–æ—Ä–æ–Ω–∞ b = ', rec.b)
print('–ü–µ—Ä–∏–º–µ—Ç—Ä = ', rec.perimeter())
print('–ü–ª–æ—â–∞–¥—å = ', rec.sq())